function openpage(){
    var x = document.getElementById("shearch").value;

    if(x=== "home"){
        window.open("https://bashamega.github.io/files/");
    }
    if(x=== "Relationships between different plants groups"){
        window.open("https://bashamega.github.io/files/Relationships-between-different-plant-groups.pdf")
    }
    if(x=== "Summary of root functions"){
        window.open("https://bashamega.github.io/files/Summary-of-root-functions.pdf")
    }
    if(x=== "Summary of root functions by type"){
        window.open("https://bashamega.github.io/files/Summary-of-root-functions-by-type.pdf")
    }
    
}